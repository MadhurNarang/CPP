#include<iostream.h>
void main()
{
 int a[10],even[10],odd[10],n,l=0,m=0;
 cout<<"ENTER NO OF INTERGERS";
 cin>>n;
 for(int i=0;i<n;i++)
 {
  cin>>a[i];
 }
 for(i=0;i<n;i++)
 {
  if(i%2==0)
  {
	even[l++]=a[i];
  }
  else
  {
  odd[m++]=a[i];
  }
 }
 cout<<"even\t";
 for(i=0;i<l;i++)
 {
  cout<<even[i]<<" ";
 }
 cout<<"\nodd \t";
 for(i=0;i<m;i++)
 {
  cout<<odd[i]<<" ";
 }
}